! Newton's Method to evaluate root of the equation
! #################################################################
!---------------------------------------------------------------------------------
!
real(8) function Fs(F,x,h)
    implicit none

    ! -----------------------------------------------------------------------------
    ! Declaration of parameters
    ! -----------------------------------------------------------------------------
    real(8),external::F     ! function
    real(8)::x              ! position
    real(8)::h              ! step width

    ! -----------------------------------------------------------------------------
    ! Calculate the derivative of the function
    ! -----------------------------------------------------------------------------
    Fs = (F(x+h/2) - F(x-h/2))/h

end function

